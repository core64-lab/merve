"""Unit tests for AGENTS.md generation, `merve init-agents`, and the doctor check.

Covers RFC 0002 W-A1 (A1 + A2): the generated agent/operator guide scaffolded by
`merve init`, regenerated by `merve init-agents`, and checked for staleness by
`merve doctor`.
"""

from typer.testing import CliRunner

from mlserver.agents_md import (
    AGENTS_MD_TEMPLATE_VERSION,
    generate_agents_md,
    init_agents_md,
    parse_agents_md_version,
)
from mlserver.cli import app
from mlserver.doctor import CheckStatus, check_agents_md

runner = CliRunner()


class TestGenerateAgentsMdSingle:
    """Single-classifier variant rendering."""

    def _render(self):
        return generate_agents_md(
            classifier_names=["demo"],
            default_classifier=None,
            multi=False,
            mlserver_version="1.2.3",
        )

    def test_no_multi_content(self):
        content = self._render()
        assert "## Multi-classifier rules" not in content
        assert "MLSERVER_CLASSIFIER" not in content

    def test_serve_line_has_no_classifier_flag(self):
        content = self._render()
        assert "- Serve locally: `merve serve`" in content
        assert "merve serve --classifier" not in content

    def test_version_stamp_line(self):
        content = self._render()
        assert f"AGENTS.md template version: {AGENTS_MD_TEMPLATE_VERSION}" in content

    def test_stamps_merve_version(self):
        content = self._render()
        assert "Generated by merve 1.2.3" in content

    def test_do_not_edit_by_hand(self):
        content = self._render()
        assert "Do not edit by hand" in content

    def test_markers_absent_from_output(self):
        content = self._render()
        assert "[SINGLE]" not in content
        assert "[MULTI]" not in content

    def test_ends_with_single_newline(self):
        content = self._render()
        assert content.endswith("\n")
        assert not content.endswith("\n\n")

    def test_no_unrendered_placeholders(self):
        content = self._render()
        for placeholder in (
            "{merve_version}",
            "{TEMPLATE_VERSION}",
            "{classifier_list}",
            "{default_classifier}",
            "{first_classifier}",
        ):
            assert placeholder not in content

    def test_canonical_json_snippet_preserved(self):
        # The {...} JSON in the curl example must survive rendering untouched.
        content = self._render()
        assert '{"records": [{"feature_a": 1.0, "feature_b": "x"}]}' in content


